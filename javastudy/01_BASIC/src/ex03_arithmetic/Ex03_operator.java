package ex03_arithmetic;

public class Ex03_operator {

	public static void main(String[] args) {
		
		// 대입 연산 (전달, 저장)
		// = : 등호의 오른쪽 값을 왼쪽에 변수에 저장
		
		
		
		
		// 유명한 대입 예제
		// 변수에 저장된 값을 교환하시오
		
		int x = 1;
		int y = 2;
		int t;  // temporary(임시)
		
		t = x;
		x = y;  // 갚을 가지고있을 때 저장하면 덮어씀
		y = t;
		
		System.out.println(x);
		System.out.println(y);
		
		
		
		
		
		
		
		
		
		
	}

}
