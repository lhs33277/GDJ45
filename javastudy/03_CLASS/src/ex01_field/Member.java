package ex01_field;

// 클래스 Member
// Member 타입이 새로 생성된다.

public class Member {
	
	// 필드 (field)         // 회원 한명이 5개의 데이터를 가질 수 있다.
	// 1. 클래스가 가지는 값이다.
	// 2. 일반변수와 다르게 자동으로 초기화된다. - 값을 이미 가지고 있다.
	
	
	String userId;  // String은 null값을 가짐
	String name;    // null
	int age;        // 0으로 초기화
	double height;  // 0.0으로 초기화
	boolean isVIP;  // false로 초기화               (위 전부다 값이 없다는 뜻)
	
}
	
	


